package variable02;

public class Variable03 {

	public static void main(String[] args) {
		byte by1 = 10;
		System.out.println(by1);
		
		short sh = 30000;
		System.out.println(sh);
		
		char ch1 = '김';
		int chInt = ch1;
		System.out.println("'김'의 유니코드 : " + chInt);
		
		double dou1 = 5/3.0;
		System.out.println(dou1);
	}
}
